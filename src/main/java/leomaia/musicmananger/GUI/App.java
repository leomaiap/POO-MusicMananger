    private void buscaFiltroB(String buscado){
        DefaultListModel<Musica> filtroB = new DefaultListModel<>();
        for (int i = 0; i < LMBiblioteca.size(); i++) {
            Musica musica = LMBiblioteca.getElementAt(i);
            String elem = musica.toString().toLowerCase();
            if(elem.contains(buscado.toLowerCase())){
                filtroB.addElement(musica);
            }
        }
        jListBiblioteca.setModel(filtroB);
    }
    
