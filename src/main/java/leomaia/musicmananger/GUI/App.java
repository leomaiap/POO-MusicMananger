    private void apagarUsuario1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_apagarUsuario1ActionPerformed
        this.usuarioSelecionado = jListUsuario.getSelectedValue();
        if(!(user.getLogin().equals(usuarioSelecionado.getLogin()))){
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja apagar usuario?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION) {
                Usuario.removerUsuario(usuarioSelecionado.getLogin());
                LMUsuarios = PersistenciaUsuario.usuarioToListModel();
                jListUsuario.setModel(LMUsuarios);
            }
        }
    }//GEN-LAST:event_apagarUsuario1ActionPerformed

