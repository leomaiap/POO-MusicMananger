    private void cadastrarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarBTActionPerformed
        String nome = cadastrarNome.getText();
        String login = cadastrarLogin.getText();
        String senha = cadastrarSenha.getText();
        if (nome.isEmpty() || login.isEmpty() || senha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        } else {
            Usuario.cadastroNovoUsuario(nome, login, senha);
            cadastrarNome.setText(null);
            cadastrarLogin.setText(null);
            cadastrarSenha.setText(null);
            JOptionPane.showMessageDialog(null, "Usuário Cadastrado");
        }         
    }//GEN-LAST:event_cadastrarBTActionPerformed

    private void entarBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entarBTActionPerformed
        String login = entarLogin.getText();
        String senha = entarSenha.getText();
        boolean auth = Login.verificarCredenciais(login, senha);
        if(auth){
            user = Usuario.carregarUsuario(login);
            JOptionPane.showMessageDialog(null, "Bem-vindo "+user.getNome());
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Usuário ou Senha incoretos");
        }
    }//GEN-LAST:event_entarBTActionPerformed

